import CommandOutput from "../../CommandOutput";
import Command from "../../Command";

export default class RandcatCommand extends Command {

    public constructor() {
        super("randcat", "fun");
    }

    public override execute(args: Array<string>, flags: Array<string>, piped: boolean): CommandOutput {
        return CommandOutput.notices(this.randomCat());
    }

    private randomCat(): Array<string> {
        return cats[Math.round(Math.random() * (cats.length - 1))];
    }
}

